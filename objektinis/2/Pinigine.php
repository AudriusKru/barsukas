<?php


class Pinigine 
{
    private $popieriniaiPinigai;
    private $metaliniaiPinigai;

    private function ideti($kiekis) 
    {
        if ($kiekis < 2) {
            return "ERROR";
        }

        if ($kiekis <= 2) {
            return $this->metaliniaiPinigai += $kiekis;
        }
        elseif {
            return $this->popieriniaiPinigai +=$kiekis;
        }
    }
}